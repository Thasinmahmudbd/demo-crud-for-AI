    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(Request $request, $id)
    {

        # Check if old image exists.
        $check = gallery::find($id);

        if($check->image!=null){

            $old_file_name = $check->image;
            $file_path = public_path('/media/gallery/'.$old_file_name);
            unlink($file_path);

        }

        gallery::destroy($id);

        $request->session()->put('msgHook','green');
        return redirect(route('indexProList'))->with('msg', 'Image deleted');
    }
